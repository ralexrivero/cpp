#include "main.h"

/**
 * add: basic addition
 * return: always 0
 */
float add(float a, float b) {
    return (a + b);
}


float mul(float a, float b) {
    return (a * b);
}

float _divf(float a, float b) {
    return (a / b);
}

int _divi(int a, int b) {
    return (a / b);
}

float sub(float a, float b) {
    return (a - b);
}

int mod(int a, int b) {
    return (a % b);
}
